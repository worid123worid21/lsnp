#include <wiringPi.h>
#include <softTone.h>

#define SPKR 	6 	/* GPIO25 */
#define TOTAL 	32 

// int notes[] = { 	/* 학교종을 연주하기 위한 계이름 */
// 	391, 391, 440, 440, 391, 391, 329.63, 329.63, \
